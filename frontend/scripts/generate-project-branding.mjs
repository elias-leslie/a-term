import fs from 'node:fs/promises'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const scriptDir = path.dirname(fileURLToPath(import.meta.url))
const frontendRoot = path.resolve(scriptDir, '..')
const repoRoot = path.resolve(frontendRoot, '..')
const identityPath = path.join(repoRoot, 'project.identity.json')
const outputPath = path.join(frontendRoot, 'lib', 'generated', 'project-branding.generated.ts')
const checkOnly = process.argv.includes('--check')

function requireString(value, label) {
  if (typeof value !== 'string' || value.length === 0) {
    throw new Error(`Missing required string field: ${label}`)
  }
  return value
}

function requireNumber(value, label) {
  if (typeof value !== 'number' || Number.isNaN(value)) {
    throw new Error(`Missing required number field: ${label}`)
  }
  return value
}

function toTsString(value) {
  return `'${String(value).replaceAll('\\', '\\\\').replaceAll("'", "\\'")}'`
}

function buildSource(identity) {
  const project = identity.project ?? {}
  const branding = identity.branding ?? {}
  const runtime = identity.runtime ?? {}

  const productId = requireString(project.id, 'project.id')
  const productName = requireString(project.display_name, 'project.display_name')
  const productShortName = requireString(branding.short_name, 'branding.short_name')
  const productDescription = requireString(branding.description, 'branding.description')
  const backendPort = requireNumber(runtime.backend_port, 'runtime.backend_port')
  const frontendPort = requireNumber(runtime.frontend_port, 'runtime.frontend_port')

  return `/* AUTO-GENERATED by frontend/scripts/generate-project-branding.mjs. */\n` +
    `export const PRODUCT_ID = ${toTsString(productId)} as const\n` +
    `export const PRODUCT_NAME = ${toTsString(productName)} as const\n` +
    `export const PRODUCT_SHORT_NAME = ${toTsString(productShortName)} as const\n` +
    `export const PRODUCT_DESCRIPTION =\n` +
    `  ${toTsString(productDescription)} as const\n` +
    `export const PRODUCT_PORTS = {\n` +
    `  backend: ${backendPort},\n` +
    `  frontend: ${frontendPort},\n` +
    `} as const\n`
}

const identity = JSON.parse(await fs.readFile(identityPath, 'utf8'))
const nextSource = buildSource(identity)
let currentSource = ''
try {
  currentSource = await fs.readFile(outputPath, 'utf8')
} catch (error) {
  if (error?.code !== 'ENOENT') {
    throw error
  }
}

if (checkOnly) {
  if (currentSource !== nextSource) {
    console.error(`Generated branding module is out of date: ${outputPath}`)
    process.exit(1)
  }
  process.exit(0)
}

await fs.mkdir(path.dirname(outputPath), { recursive: true })
if (currentSource !== nextSource) {
  await fs.writeFile(outputPath, nextSource)
}
